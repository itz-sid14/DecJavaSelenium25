package CollectionInJava;

import java.util.HashSet;
import java.util.Set;

public class HashSetInJava {
	
	public static void main(String[] args) {
		
		Set<String> hs= new HashSet<>();
		
		hs.add("One");
		hs.add("Two");
		hs.add("Four");
		hs.add("Three");
		
		System.out.println(hs);
		
		
	}

}
