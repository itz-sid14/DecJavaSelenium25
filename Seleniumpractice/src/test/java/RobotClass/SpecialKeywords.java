package RobotClass;

public class SpecialKeywords {
	
public static void main(String[] args) {
	
	System.out.println("Hello");
	
	// Here \t is used to give the tab space between word
	System.out.println("Hello"+ "\t"+"Java");
	
	//Here \n is used to print the words in new line
	System.out.println("Hello"+ "\n"+"Java");
	
}

}
